const categoryToItems = new Map<string, EmojiItem[]>();

// Build all lookup maps
EMOJI_DATABASE.forEach((cat) => {
  categoryToItems.set(cat.category, cat.items);
  cat.items.forEach((item) => {
    emojiToNames.set(item.emoji, item.names);
    emojiToCategory.set(item.emoji, cat.category);
    item.names.forEach((name) => {
      const lowerName = name.toLowerCase();
      // First emoji wins for direct lookup
      if (!nameToEmoji.has(lowerName)) {
        nameToEmoji.set(lowerName, item.emoji);
      }
      // Track all emojis that share this name (for similarity)
      if (!nameToAllEmojis.has(lowerName)) {
        nameToAllEmojis.set(lowerName, []);
      }
      nameToAllEmojis.get(lowerName)!.push(item.emoji);
    });
  });
});

